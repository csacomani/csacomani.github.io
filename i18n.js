const supportedLanguages = Object.keys(translations);
const defaultLanguage = "pt-BR";

function detectLanguage() {
  const saved = localStorage.getItem("inovat-language");
  if (supportedLanguages.includes(saved)) {
    return saved;
  }

  const browserLanguage = navigator.language || "";
  if (browserLanguage.toLowerCase().startsWith("es")) {
    return "es";
  }
  if (browserLanguage.toLowerCase().startsWith("en")) {
    return "en";
  }
  return defaultLanguage;
}

function setLanguage(language) {
  const dictionary = translations[language] || translations[defaultLanguage];
  document.documentElement.lang = language;
  document.title = dictionary.metaTitle;
  document
    .querySelector('meta[name="description"]')
    ?.setAttribute("content", dictionary.metaDescription);
  document
    .querySelector('meta[property="og:title"]')
    ?.setAttribute("content", dictionary.metaTitle);
  document
    .querySelector('meta[property="og:description"]')
    ?.setAttribute("content", dictionary.metaDescription);

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (key && dictionary[key]) {
      node.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    const isActive = button.getAttribute("data-lang") === language;
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("inovat-language", language);
}

document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.addEventListener("click", () => {
      const language = button.getAttribute("data-lang") || defaultLanguage;
      setLanguage(language);
    });
  });

  setLanguage(detectLanguage());
});
